                                <p>３、甄用流程：如辦理面談、專業訓練、實習及考核等事項，由各組室依實際需求訂定並送交館方備查。</p>
                                <p>４、同組室之轉調：服務轉解說，需參與專業訓練、實習及考核等事項；解說轉服務，需通過書面審查及面談。</p>
                                <p>５、跨組值勤時數計算：於原組室每年值勤 60 小時，每跨一組室時數增加 30 小時，跨組室不限組室數量。年度值勤時數若未達規定者，取消跨組服務資格。</p>
