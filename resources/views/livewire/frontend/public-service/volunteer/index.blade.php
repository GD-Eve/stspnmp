                                <p>(六) 實習及考核：完成特殊訓練課程後進行實習試用，於期間內完成規定實習時數始具備考核資格。考核方式得包含參與課程出席率、實習表現、筆試及口試等。</p>
                                <p>(七) 志工聘用：通過考核並完成志願服務基礎訓練領有志願服務紀錄冊者，正式聘為本館志工。</p>
